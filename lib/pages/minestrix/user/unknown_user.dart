class ProfileDiscovery extends StatelessWidget {
  const ProfileDiscovery({
    Key? key,
    required this.userId,
    required this.client,
  }) : super(key: key);

  final String? userId;
  final Client client;

  @override
  Widget build(BuildContext context) {
    return FutureBuilder<List<SpaceRoom>?>(
        future: userId != null
            ? ProfileSpace.getProfileSpaceHierarchy(client, userId!)
            : null,
        builder: (context, snapshot) {
          List<SpaceRoom> profiles = (snapshot.data ?? [])
              .where((SpaceRoom room) => room.roomType == MatrixTypes.account)
              .toList();
// we ignore generated by getProfileSpaceContent when the space doesn't exist
          return Column(
            children: [
              for (SpaceRoom space in (snapshot.data ?? []))
                ConstrainedBox(
                  constraints: BoxConstraints(maxWidth: 200),
                  child: ListTile(
                      title: Text(space.id), subtitle: Text(space.name)),
                ),
              for (SpaceRoom space in profiles)
                if ([JoinRules.knock, JoinRules.public]
                        .contains(space.joinRule) &&
                    (client.getRoomById(space.id) == null ||
                        ![
                          Membership.knock,
                          Membership.join
                        ].contains(client.getRoomById(space.id)?.membership)))
                  CustomFutureButton(
                      icon: Icon(Icons.person_add,
                          color: Theme.of(context).colorScheme.onPrimary),
                      color: Theme.of(context).primaryColor,
                      children: [
                        Text(space.name,
                            style: TextStyle(
                                color:
                                    Theme.of(context).colorScheme.onPrimary)),
                        Text("Follow",
                            style: TextStyle(
                                fontWeight: FontWeight.bold,
                                color: Theme.of(context).colorScheme.onPrimary))
                      ],
                      onPressed: () async {
                        switch (space.joinRule) {
                          case JoinRules.public:
                            client.joinRoom(space
                                .id); // TODO:Â update me to support joining over federation (need the via field)
                            await client.waitForRoomInSync(space.id);

                            break;
                          case JoinRules.knock:
                            client.knockRoom(space.id);
                            await client.waitForRoomInSync(space.id);

                            break;
                          default:
                        }
                        client.knockRoom(space.id);
                      },
                      expanded: false),
            ],
          );
        });
  }
}
